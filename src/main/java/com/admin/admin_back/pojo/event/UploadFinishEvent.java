package com.admin.admin_back.pojo.event;

import org.springframework.context.ApplicationEvent;

/**
 * @author 陈群矜
 */
public class UploadFinishEvent extends ApplicationEvent {

    private String key;

    public UploadFinishEvent(Object source, String key) {
        super(source);
        this.key = key;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

}
